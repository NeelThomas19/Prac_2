#Python program to create an intersection, Union, difference of sets.
Set1 ={1, 2 , 3 , 4 , 5}
Set2 = {4 , 5 , 6 ,7 ,8}
#OR operator is used for union.
print(Set1 | Set2)
#AND operator is used for intersection.
print(Set1 & Set2)
#(-) is used for difference.
print(Set1 - Set2)